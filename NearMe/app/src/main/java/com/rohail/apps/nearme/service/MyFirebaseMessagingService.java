import android.app.NotificationManager;
import android.app.PendingIntent;
import android.content.Context;
import android.content.Intent;
import android.graphics.Bitmap;
import android.graphics.BitmapFactory;
import android.media.RingtoneManager;
import android.net.Uri;
import android.support.v4.app.NotificationCompat;

import com.google.firebase.messaging.FirebaseMessagingService;
import com.google.firebase.messaging.RemoteMessage;
import com.rohail.apps.nearme.R;
import com.rohail.apps.nearme.activities.MapActivity;
import com.rohail.apps.nearme.utilities.Constants;
import com.rohail.apps.nearme.utilities.Logger;


public class MyFirebaseMessagingService extends FirebaseMessagingService {

    /**
     * Called when message is received.
     *
     * @param remoteMessage Object representing the message received from Firebase Cloud Messaging.
     */
    // [START receive_message]
    @Override
    public void onMessageReceived(RemoteMessage remoteMessage) {
        // TODO(developer): Handle FCM messages here.
        // If the application is in the foreground handle both data and notification messages here.
        // Also if you intend on generating your own notifications as a result of a received FCM
        // message, here is where that should be initiated. See sendNotification method below.
//        AppLogger.i("From: " + remoteMessage.getFrom());
        // TODO: 5/24/2016 undo below lines
        Logger.i("Notification Message: " + remoteMessage.getNotification().getBody());
        sendNotification(remoteMessage);
    }
    // [END receive_message]

    /**
     * Create and show a simple notification containing the received FCM message.
     *
     * @param remoteMessage FCM message body received.
     */
    private void sendNotification(RemoteMessage remoteMessage) {

        String message = remoteMessage.getNotification().getBody();
        PendingIntent pendingIntent = null;
        Intent intent = null;

        intent = new Intent(this, MapActivity.class);
        intent.addFlags(Intent.FLAG_ACTIVITY_CLEAR_TOP);
        intent.putExtra(Constants.IntentKeys.KEY_NOTIFICATION_MSG, message);

        if (remoteMessage.getData() != null && remoteMessage.getData().get("update") != null &&
                remoteMessage.getData().get("update").equals(Constants.NotificationId.update)) {
            intent.putExtra(Constants.IntentKeys.KEY_NOTIFICATION_ID,
                    remoteMessage.getData().get("update"));
        }

        pendingIntent = PendingIntent.getActivity(this, 0 /* Request code */, intent,
                PendingIntent.FLAG_ONE_SHOT);


        Bitmap bm = BitmapFactory.decodeResource(getResources(), R.mipmap.ic_launcher);
        Uri defaultSoundUri = RingtoneManager.getDefaultUri(RingtoneManager.TYPE_NOTIFICATION);
        NotificationCompat.Builder notificationBuilder = new NotificationCompat.Builder(this)
                .setSmallIcon(R.drawable.icon)
                .setLargeIcon(bm)
                .setContentTitle(getResources().getString(R.string.app_name))
                .setContentText(message)
                .setSubText("Tap to see.")
                .setAutoCancel(true)
                .setSound(defaultSoundUri)
                .setContentIntent(pendingIntent);

        NotificationManager notificationManager =
                (NotificationManager) getSystemService(Context.NOTIFICATION_SERVICE);

        notificationManager.notify(0 /* ID of notification */, notificationBuilder.build());
    }


}
